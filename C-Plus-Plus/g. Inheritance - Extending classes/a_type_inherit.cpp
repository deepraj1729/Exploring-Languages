/*
        Inheritance:
        --------------------------------------------------------------------------------------------------
        It is the property of extending (Reusing) previously built classes in a program.


        Base Class:
        --------------------------------------------------------------------------------------------------
        The class from which it inherits (reuses) is called Base class


        Derived Class:
        --------------------------------------------------------------------------------------------------
        The class which is created after inheriting from the base class is called the Derived class.



        The types of inheritances are:
        --------------------------------------------------------------------------------------------------
        1. Single inheritance
        2. Mutiple inheritance
        3. Multi-level inheritance
        4. Hierarchical inheritance
        5. Hybrid inheritance



        Visibility of base classes during inheritance: (Public,protected,private derivation)

        -----------------------------------------------------------------------------------------------------------
        Base class Visibility   |       public derivation     |     private derivation    |    protected derivation 
        -----------------------------------------------------------------------------------------------------------
        private     ------------>       Not inherited               Not inherited              Not inherited

        protected   ------------>       protected                   private                    protected

        public      ------------>       public                      private                    protected
        
        -----------------------------------------------------------------------------------------------------------

            
*/